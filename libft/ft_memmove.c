#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t len)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	if (dest <= src)
		return (ft_memcpy(d, s, len));
	while (len--)
		d[len] = s[len];
	return (dest);
}
