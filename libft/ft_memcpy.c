#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*s;
	unsigned char	*d;

	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	while ((unsigned int)n-- > 0)
		*d++ = *s++;
	return ((unsigned char *)dest);
}
